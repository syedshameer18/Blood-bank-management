package model;

public class BloodStock {
    private String blood_group;
    private int units_available;

    public String getBlood_group() { return blood_group; }
    public void setBlood_group(String blood_group) { this.blood_group = blood_group; }

    public int getUnits_available() { return units_available; }
    public void setUnits_available(int units_available) { this.units_available = units_available; }
}